<p>Hi, inside this section you will find a small and very easy to understand guide about the way to ad po and mo files to this theme. Make sure you have translated your wordpress set-up. read throught he following list about the way to achieve this. It's really easy, in some cases you can even download a copy of wordpress in your language.</p>

<ul>
<li><a href="http://codex.wordpress.org/WordPress_in_Your_Language">WordPress in Your Language</a></li>
<li><a href="http://codex.wordpress.org/Translating_WordPress">Translating WordPress</a></li>
<li><a href="http://weblogtoolscollection.com/archives/2007/03/06/wp-translations-mo-and-po-files/">WP Translations: mo and po files</a></li>
<li><a href="http://codex.wordpress.org/Installing_WordPress_in_Your_Language">Installing WordPress in Your Language</a></li>
</ul>

<p>Next you find the po and mo file of this theme inisde the <strong>lang</strong> folder inisde the root of the theme. Download these files and open the po file with <a href="http://www.poedit.net/">Poedit</a>. When you've opened the .po file you will see a screen like this.</p>

<img class="alignnone size-full wp-image-2547" title="poedit" src="http://wptitans.com/yvora/files/2011/04/poedit.png" alt="" />

<p>Next thing you need to do is to translate all items until the last line. After that you need to save the file as both .po and .mo. Rename it the the po and mo files to your language code. For example when your dutch and you have set the translate option inside your config.php to <strong>define ('WPLANG', 'nl_NL');</strong> then you need to rename your po and mo to <strong>nl_NL.po</strong> and <strong>nl_NL.mo</strong></p>

<p>Now you've saved your po and mo you need to upload them to the root of the theme. Not inside the lang folder but in the root of the theme. If this doesn't work then rename the lang folder to language and ad the files in there. Below you will find a few more useful links to get started with making the po and mo work.</p>

